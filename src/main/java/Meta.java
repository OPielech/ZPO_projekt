public class Meta {
    String name;
    String license;
    String website;
    int page;
    int limit;
    int found;

    public String getName() {
        return name;
    }

    public String getLicense() {
        return license;
    }

    public String getWebsite() {
        return website;
    }

    public int getPage() {
        return page;
    }

    public int getLimit() {
        return limit;
    }

    public int getFound() {
        return found;
    }
}
